bool _isIgnorableFlutterAssertion(FlutterErrorDetails details) {
  final text = details.exceptionAsString();
  return text.contains("'_dependents.isEmpty': is not true");
}

Future<void> _settleRouteTransition() async {
  await Future<void>.delayed(const Duration(milliseconds: 180));
}

void main() {
  debugPrint(
    '[APP] boot kDebugMode=$kDebugMode kProfileMode=$kProfileMode kReleaseMode=$kReleaseMode',
  );
  // Guard against uncaught async errors that would otherwise crash the app
  FlutterError.onError = (FlutterErrorDetails details) {
    if (_isIgnorableFlutterAssertion(details)) {
      debugPrint('[IGNORED][FLUTTER] ${details.exceptionAsString()}');
      debugPrint(details.stack?.toString());
      return;
    }
    FlutterError.presentError(details);
    debugPrint('[UNCAUGHT] ${details.exceptionAsString()}');
    debugPrint(details.stack?.toString());
  };
  runZonedGuarded(
    () {
      runApp(const ArtAirCleanerApp());
    },
    (Object error, StackTrace stack) {
      debugPrint('[UNCAUGHT-ZONE] $error');
      debugPrint(stack.toString());
    },
  );
}

