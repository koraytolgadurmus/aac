  // Language maps are normalized against English so every key always exists.
  // Missing translations are reported in debug mode for incremental completion.
  static final Map<String, Map<String, String>> _normalizedMap =
      _buildNormalizedMap();

  static Map<String, Map<String, String>> _buildNormalizedMap() {
    final en = Map<String, String>.from(
      (_map['en'] as Map?)?.cast<String, String>() ?? const <String, String>{},
    );
    final out = <String, Map<String, String>>{};

    for (final langCode in supported.keys) {
      final src = Map<String, String>.from(
        (_map[langCode] as Map?)?.cast<String, String>() ??
            const <String, String>{},
      );
      final merged = Map<String, String>.from(en)..addAll(src);
      out[langCode] = Map.unmodifiable(merged);
    }

    // Keep explicit fallback available even if supported list changes.
    out.putIfAbsent('en', () => Map.unmodifiable(en));

    assert(() {
      _logMissingTranslations(en);
      return true;
    }());

    return Map.unmodifiable(out);
  }

  static void _logMissingTranslations(Map<String, String> en) {
    for (final langCode in supported.keys) {
      if (langCode == 'en') continue;
      final src = Map<String, String>.from(
        (_map[langCode] as Map?)?.cast<String, String>() ??
            const <String, String>{},
      );
      final missing = <String>[];
      for (final key in en.keys) {
        if (!src.containsKey(key) || (src[key] ?? '').trim().isEmpty) {
          missing.add(key);
        }
      }
      if (missing.isNotEmpty) {
        debugPrint(
          '[I18N] $langCode missing=${missing.length} '
          'sample=${missing.take(8).join(',')}',
        );
      }
    }
  }
}

String _lt(BuildContext context, String raw) {
  final code = Localizations.localeOf(context).languageCode;
  return I18n(code).literal(raw);
